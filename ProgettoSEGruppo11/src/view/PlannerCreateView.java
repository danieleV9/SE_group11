    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
        String area = areaField.getText();
        String tipology = tipologyField.getText();
        String estimatedTime = timeField.getText();
        int time = Integer.parseInt(estimatedTime);
        String weekNumber = weekField.getText();
        int numberWeek = Integer.parseInt(weekNumber);
        String factory = factoryField.getText();
        String type = typeField.getText();
        String workNotes = notesField.getText();
        String description = descriptionField.getText();
        String interruptible = jCheckBox.getActionCommand();
        if(description.equals("") || factory.equals("") || type.equals("") || weekNumber.equals("") || area.equals("") || tipology.equals("") || estimatedTime.equals("")){
            JOptionPane.showMessageDialog(this, "Inserire tutti i campi!", "Attenzione!", JOptionPane.ERROR_MESSAGE);
            return;
        } 
    try{
        String query = "INSERT INTO ATTIVITA_MANUTENZIONE (settimana,notelavoro,tipoattivita,interrompibile,"
                + "idattivita,fabbrica,area,usernamema,tipotipologia,nomeprocedura,tempostimato,descrizione,"
                + "dataattivita,statoticket) values (?,?,?,0,(NEXTVAL(idattivita)+1),?,?,?,?,?,?,?,?,?)";
       // Class.forName("com.mqsl.jdbc.Driver");
        conn = DriverManager.getConnection("jdbc:mysql://localhost/applicazione","root","");
        pst= conn.prepareStatement(query);
        pst.setInt(1,numberWeek);
        pst.setString(2,workNotes);
        pst.setString(3,type);
      //pst.setBoolean(4,true);
        pst.setString(4,factory);
        pst.setString(5,area);
        pst.setNull(6,Types.NULL);
        pst.setString(7,tipology);
        pst.setNull(8,Types.NULL);
        pst.setInt(9, time);
        pst.setString(10,description);
        pst.setNull(11,Types.NULL);
        pst.setNull(12,Types.NULL);
        
        pst.executeUpdate();
                
        JOptionPane.showMessageDialog(this,"Activity Created Succesfully!");
        
    } catch(Exception ex){
         JOptionPane.showMessageDialog(null,ex);
    }
    }//GEN-LAST:event_createActionPerformed

